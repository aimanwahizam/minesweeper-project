/* -------------------------------------------------------------------------- */
/*                                HTML Elements                               */
/* -------------------------------------------------------------------------- */

const gameBox = document.querySelectorAll(".game__box"); // ARRAY OF BOXES

/* -------------------------------------------------------------------------- */
/*                                  Functions                                 */
/* -------------------------------------------------------------------------- */

const onClickGameBox = (event) => {
    event.target.setAttribute("class", "game__box--click");
}

/* -------------------------------------------------------------------------- */
/*                               Event Listeners                              */
/* -------------------------------------------------------------------------- */

gameBox.forEach(box => {
    box.addEventListener("click", onClickGameBox);
});
